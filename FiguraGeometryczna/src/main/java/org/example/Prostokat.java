package org.example;

public class Prostokat extends FiguraGeometryczna {

    public Prostokat(int wall1, int wall2) {
        super(wall1, wall2);
    }

    @Override
    public int area(int wall1, int wall2) {
        return area(wall1, wall2);
    }

    @Override
    public int perimetr(int wall1, int wall2) {
        return super.perimetr(wall1, wall2);
    }
}
